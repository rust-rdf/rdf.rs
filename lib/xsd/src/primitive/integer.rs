/// Rust type for representing values of the `xsd:integer` datatype.
pub type Integer = valuand::Integer;

/// Rust type for representing values of the `xsd:long` datatype.
pub type Long = i64;

/// Rust type for representing values of the `xsd:int` datatype.
pub type Int = i32;

/// Rust type for representing values of the `xsd:short` datatype.
pub type Short = i16;

/// Rust type for representing values of the `xsd:byte` datatype.
pub type Byte = i8;

/// Rust type for representing values of the `xsd:nonNegativeInteger` datatype.
pub type NonNegativeInteger = valuand::Natural;

/// Rust type for representing values of the `xsd:unsignedLong` datatype.
pub type UnsignedLong = u64;

/// Rust type for representing values of the `xsd:unsignedInt` datatype.
pub type UnsignedInt = u32;

/// Rust type for representing values of the `xsd:unsignedShort` datatype.
pub type UnsignedShort = u16;

/// Rust type for representing values of the `xsd:unsignedByte` datatype.
pub type UnsignedByte = u8;
